package org.example;

public class E002_Variables
{
    static void main(String[] args){
        //Variables types
        String S="text container";
        //numerical data
        int I=12;
        Double D=2.2;
        //Booleans
        Boolean B=true;
        //VAR, variable without defined type
        var V01="Hello Word";
        var V02=25;
        //Calls
        System.out.println(S);
        System.out.println(I);
        System.out.println(D);
        System.out.println(B);
        System.out.println(V01);
        System.out.println(V02);
    }
}